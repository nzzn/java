package switchtest;

public class SwitchTest01 {

	public static void main(String[] args) {
		
		int num = 200;
		
		switch (num) {
		case 100:
			System.out.println("100이 들어있어요.");
			break;
		case 200:
			System.out.println("200이 들어있어요.");
			break;
		case 300:
			System.out.println("300이 들어있어요.");
			break;
		default:
		System.out.println("무슨 숫자인지 모르겠어요.");
			
		
		}
		
	
		
	}

}
