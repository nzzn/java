public class ArrayTest {

	public static void main(String[] args) {
		
		int[] seat = {1, 2, 3, 4, 5};
		
		for (int i = 0; i < seat.length; i++) {
			
			System.out.println(seat[i]);
			
		}
		
		System.out.println("----------");
		System.out.println(seat[0]);
		System.out.println(seat[4]);
	
	}

}
